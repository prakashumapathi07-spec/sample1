import java.util.Scanner;
import java.util.*;
public class sum {
 public static void main(String[] args) {
 Scanner sc = new Scanner(System.in);
 System.out.println("Enter the number:");
 int num = sc.nextInt();
 int sum = 0;
 for (int i = 0; i <= num; i += 2) {
 sum += (i * i);
 }
 System.out.println(sum);
 }
}
